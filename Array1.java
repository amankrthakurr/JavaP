import java.util.*;
class Array1
{
    public static void main(String[] args)
    {
        int[] A ={1,2,3,4,5};
        for(int x:A)
        {
            System.out.println(x);
        }
        for(int i = 0; i<A.length; i++)
        {
            System.out.println(A[i]);
        }
    }
}